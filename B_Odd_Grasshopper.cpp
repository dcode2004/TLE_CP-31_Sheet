void solve() {
    int x, n;
    cin >> x >> n;
    int y = (n / 4) * 4 + 1;
    n = n % 4;
    while(n) {
        if(x & 1) x += y;
        else x -= y;
        y++;
        n--;
    }
    cout << x << endl;
}

