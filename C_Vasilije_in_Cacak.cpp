#define pb push_back
#define all(x) x.begin(), x.end()
#define rall(x) x.rbegin(), x.rend()
#define yes cout << "YES\n"
#define no cout << "NO\n"

void solve() {
    int n, k ,x;
    cin >> n >> k >> x;
    int min_sum = k*(k+1)/2;
    int max_sum = n*(n+1)/2 - (n-k)*(n-k+1)/2;
    if(x >= min_sum && x <= max_sum) yes;
    else no;
}

signed main() {
    std::ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int tt = 1;
    cin >> tt;
    while(tt--) {
        solve();
    }
    return 0;
}