void solve() {
    int n;
    cin >> n;
    if (n < 10) {
        cout << n << endl;
        return;
    }
    int a = 1000000;
    while (n / a == 0) a = a / 10;
    int add = n / a;
    int cnt = 0;
    while (a != 1) {
        a = a / 10;
        cnt++;
    }
    cout << 9 * cnt + add << endl;
}

